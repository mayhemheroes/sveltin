package utils

// PlusOne adds one to the integer parameter.
func PlusOne(x int) int {
	return x + 1
}

// MinusOne subtract one to the integer parameter.
func MinusOne(x int) int {
	return x - 1
}

// Sum adds two integer values.
func Sum(x int, y int) int {
	return x + y
}
